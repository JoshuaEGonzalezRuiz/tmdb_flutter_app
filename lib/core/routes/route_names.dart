/// A utility class that defines the route names used in the application.
///
/// [RouteNames] provides a centralized location for defining and managing
/// the string constants representing the routes in the app. This helps
/// in avoiding hard-coded strings throughout the codebase and makes it
/// easier to manage and update route paths.
class RouteNames {
  /// The route name for the home screen.
  static const String home = '/';

  /// The route name for the detail screen.
  static const String detail = '/detail';
}
